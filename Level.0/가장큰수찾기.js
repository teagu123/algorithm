function solution(array) {
	/**
	 * Math.max는 배열을 받지않음 그래서 스프레드 연산자를 통해 arr을 풀고
	 * 그 값을 indexOf로 찾아서 배열로 출력하는 로직
	 */
	let maxValue = Math.max(...array)
	return [maxValue, array.indexOf(maxValue)]
}
solution([1, 8, 3])
